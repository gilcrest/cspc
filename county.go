// County is a geographical region of a country used for administrative
// or other purposes, in certain modern nations. For the US, the Code
// is the Federal Information Processing System (FIPS) Codes for States
// and Counties. FIPS codes are numbers which uniquely identify geographic
// areas.
type County struct {
	Code             string `json:"county_cd"`
	Name             string `json:"county_name"`
	LatitudeAverage  string `json:"latitude_average"`
	LongitudeAverage string `json:"longitude_average"`
}

type countyInit struct {
	Code string `json:"county_cd"`
	Name string `json:"county_name"`
}

// FindCounties returns a list of Counties for a StateProvince
func FindCounties(sp StateProvince) ([]County, error) {
	const op errs.Op = "cspc/FindCounties"

	var (
		allCounties []countyInit
		counties    []County
	)

	err := json.Unmarshal([]byte(USCountyJSON), &allCounties)
	if err != nil {
		return nil, errs.E(op, err)
	}

	for _, county := range allCounties {
		statefip := county.Code[:2]

		if statefip == sp.FIPSCode {
			c := County{
				Code:             county.Code,
				Name:             county.Name,
				LatitudeAverage:  "",
				LongitudeAverage: "",
			}
			counties = append(counties, c)
		}
	}
	return counties, nil
}

